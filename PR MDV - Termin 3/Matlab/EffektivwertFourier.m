function RMSif = EffektivwertFourier(xt)

% function RMSif = EffektivwertFourier(xt)
% ??????????????????????????????????????????????????????
% filename:           EffektivwertFourier .m
% author:             Özgü Dogan, Timo Lausen, Boris Henckell
% organisation:       TU Berlin 
% project:            MDVPR
% date:               10.05.2012
% 
% ??????????????????????????????????????????????????????
% description:        Bestimmt im Frequenzbereich den Effektivwert
%                     eines Signals xt
% input:              xt : Signal im Zeitbereich
% output:             RMSIf: Effektivwert des Signals , berechnet aus den
%                     Fourierkoeffizienten
 
xf = abs(fftshift(fft(xt)));
RMSif = sqrt(mean(1/length(xt)*(xf.*xf)));